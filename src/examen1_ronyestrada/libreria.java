    private void checkMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_checkMouseClicked
        // TODO add your handling code here:
        String pal1=tf_nombre.getText();
         String pal2=pw_password.getText();
        if (pal1.length()<=0&&pal2.length()<=0) {
            JOptionPane.showMessageDialog(this, "ingrese usuario");
        }
       else if (cb_admin.isSelected() || admin.equals(tf_nombre) && passadmin.equals(pw_password)) {
            Acceso = true;
            JOptionPane.showMessageDialog(this, "accesso consedido como administrador");
            tf_nombre.setText("");
            pw_password.setText("");

        } else if (!cb_admin.isSelected()) {
            usuarioslog = tf_nombre.getText();
            passusuario = pw_password.getText();
            JOptionPane.showMessageDialog(this, "accesso a usuario");
        }
    }//GEN-LAST:event_checkMouseClicked

