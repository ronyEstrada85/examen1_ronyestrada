    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked

        // TODO add your handling code here:
        String pal1=tf_nombre.getText();
        String pal2=pw_password.getText();
        if (pal1.length()<=0&&pal2.length()<=0) {
            JOptionPane.showMessageDialog(this, "ingrese usuario");
        }
       else  if (cb_admin.isSelected() && admin == "" && passadmin == ""&&adminax==false) {
            admin = tf_nombre.getText();
            
            passadmin = pw_password.getText();
            JOptionPane.showMessageDialog(this, "registered as admin");

        } else if(!cb_admin.isSelected()&& admin == "" && passadmin == ""&&adminax==false){
            usuarioslog = tf_nombre.getText();
            passusuario = pw_password.getText();
            JOptionPane.showMessageDialog(this, "registered as user");
        }
        tf_nombre.setText("");
        pw_password.setText("");

    }//GEN-LAST:event_jButton2MouseClicked

